    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        HoaDon hoaDon = getData();
        if (hoaDon == null) {
            return;
        }
        if (hoaDonInterface.add(hoaDon)) {
            loadTable(hoaDonInterface.getAll());
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Them that bai");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        HoaDon hoaDon = getData();
        if(hoaDon == null){
            return;
        }
        String id = txtid.getText();
        if(hoaDonInterface.update(id, hoaDon)){
            loadTable(hoaDonInterface.getAll());
            JOptionPane.showMessageDialog(this, "Sua thanh cong");
            clearForm();
        }else{
            JOptionPane.showMessageDialog(this, "Sua that bai");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String id = txtid.getText();
        if (hoaDonInterface.delete(id)) {
            loadTable(hoaDonInterface.getAll());
            JOptionPane.showMessageDialog(this, "Xoa thanh cong");     
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Xoa that bai");
        }
        
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblBangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBangMouseClicked
        int row = tblBang.getSelectedRow();
        if (row == -1) {
            return;
        }
        txtid.setText(tblBang.getValueAt(row, 0).toString());
        txtMa.setText(tblBang.getValueAt(row, 1).toString());
        txtNgayTao.setText(tblBang.getValueAt(row, 2).toString());
        txtNgayThanhToan.setText(tblBang.getValueAt(row, 3).toString());
        txtTinhTrang.setText(tblBang.getValueAt(row, 4).toString());
        txtNguoiNhan.setText(tblBang.getValueAt(row, 5).toString());
        txtSoTien.setText(tblBang.getValueAt(row, 6).toString());
    }//GEN-LAST:event_tblBangMouseClicked

