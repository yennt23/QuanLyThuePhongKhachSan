    public void loadTable(List<com.mycompany.Domainmodel.HoaDon> list) {
        DefaultTableModel dtm = (DefaultTableModel) tblBang.getModel();
        dtm.setRowCount(0);
        for (com.mycompany.Domainmodel.HoaDon hoaDon : list) {
            Object[] rowData = {
                hoaDon.getId(),
                hoaDon.getMa(),
                hoaDon.getNgayTao(),
                hoaDon.getNgayTraPhong(),
                hoaDon.getTinhTrang(),
                hoaDon.getTenKH(),
                hoaDon.getSoTien()
            };
            dtm.addRow(rowData);
        }
    }

    public HoaDon getData() {
        String ma = txtMa.getText();
        String ngayTao = txtNgayTao.getText();
        String ngayThanhToan = txtNgayThanhToan.getText();
        String tinhTrang = txtTinhTrang.getText();
        String tenNguoiNhan = txtNguoiNhan.getText();
        String soTien = txtSoTien.getText();
        
        HoaDon hoaDon = new HoaDon(ma, Date.valueOf(ngayTao), Date.valueOf(ngayThanhToan), tinhTrang, tenNguoiNhan, Float.parseFloat(soTien));
        return hoaDon;
    }

